	 				bombas pueden ser sumergibles o de superficie y son capaces de bombear el agua a 60 metros o más, dependiendo del número y tipo de impulsores.
	 			</p>
	 			<p>
	 				Las bombas de succión superficial se instalan a nivel del suelo y tienen la ventaja de que se les puede inspeccionar y dar servicio fácilmente. 
